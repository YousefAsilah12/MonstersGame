class Monster {
    constructor(name, defense, dexterity, gold, lvl, strength, rewardXP, life){
        this.name = name;
        this.strength = strength;
        this.defense = defense;
        this.lvl = lvl;
        this.life = life;
        this.rewardXP = rewardXP;
        this.dexterity = dexterity;
        this.gold = gold;
    }
}
